    <script>
        const faqGroups = document.querySelectorAll('.faq-group');

        faqGroups.forEach(group => {
            const groupToggle = group.querySelector('.faq-group-toggle');

            groupToggle.addEventListener('click', () => {
                const isOpen = group.classList.contains('is-open');

                faqGroups.forEach(otherGroup => {
                    const otherToggle = otherGroup.querySelector('.faq-group-toggle');
                    const shouldOpen = otherGroup === group && !isOpen;

                    otherGroup.classList.toggle('is-open', shouldOpen);
                    otherToggle.setAttribute('aria-expanded', shouldOpen ? 'true' : 'false');
                });
            });

            const questions = group.querySelectorAll('.faq-question');

            questions.forEach(question => {
                const questionToggle = question.querySelector('.faq-question-toggle');

                questionToggle.addEventListener('click', (event) => {
                    event.stopPropagation();

                    const isQuestionOpen = question.classList.contains('is-open');

                    questions.forEach(otherQuestion => {
                        const otherQuestionToggle = otherQuestion.querySelector('.faq-question-toggle');
                        const shouldOpen = otherQuestion === question && !isQuestionOpen;

                        otherQuestion.classList.toggle('is-open', shouldOpen);
                        otherQuestionToggle.setAttribute('aria-expanded', shouldOpen ? 'true' : 'false');
                    });
                });
            });
        });
    </script>
